import React, { useState, useEffect } from 'react';
import { parseISO } from 'date-fns';

import income from '../../assets/income.svg';
import outcome from '../../assets/outcome.svg';
import total from '../../assets/total.svg';

import api from '../../services/api';

import Header from '../../components/Header';

import formatValue from '../../utils/formatValue';
import formatDate from '../../utils/formatDate';

import { Container, CardContainer, Card, TableContainer } from './styles';

interface Transaction {
  id: string;
  title: string;
  value: number;
  formattedValue: string;
  formattedDate: string;
  type: 'income' | 'outcome';
  category: { title: string };
  created_at: string;
}

interface Balance {
  income: number;
  formattedIncome: string;
  outcome: number;
  formattedOutcome: string;
  total: number;
  formattedTotal: string;
}

const Dashboard: React.FC = () => {
  const [transactions, setTransactions] = useState<Transaction[]>([]);
  const [balance, setBalance] = useState<Balance>({} as Balance);

  useEffect(() => {
    async function loadTransactions(): Promise<void> {
      api.get('transactions').then(response => {
        const loadedTransactions: Transaction[] = response.data.transactions;
        const loadedBalance: Balance = response.data.balance;

        loadedTransactions.forEach(tempTransaction => {
          const transaction = tempTransaction;
          transaction.formattedDate = formatDate(
            parseISO(transaction.created_at),
          );
          if (transaction.type === 'outcome') {
            transaction.formattedValue = `- ${formatValue(transaction.value)}`;
          } else {
            transaction.formattedValue = formatValue(transaction.value);
          }
        });

        loadedBalance.formattedIncome = formatValue(loadedBalance.income);
        loadedBalance.formattedOutcome = formatValue(loadedBalance.outcome);
        loadedBalance.formattedTotal = formatValue(loadedBalance.total);

        setTransactions(loadedTransactions);
        setBalance(loadedBalance);
      });
    }

    loadTransactions();
  }, []);

  return (
    <>
      <Header />
      <Container>
        <CardContainer>
          <Card>
            <header>
              <p>Entradas</p>
              <img src={income} alt="Income" />
            </header>
            <h1 data-testid="balance-income">{balance.formattedIncome}</h1>
          </Card>
          <Card>
            <header>
              <p>Saídas</p>
              <img src={outcome} alt="Outcome" />
            </header>
            <h1 data-testid="balance-outcome">{balance.formattedOutcome}</h1>
          </Card>
          <Card total>
            <header>
              <p>Total</p>
              <img src={total} alt="Total" />
            </header>
            <h1 data-testid="balance-total">{balance.formattedTotal}</h1>
          </Card>
        </CardContainer>

        <TableContainer>
          <table>
            <thead>
              <tr>
                <th>Título</th>
                <th>Preço</th>
                <th>Categoria</th>
                <th>Data</th>
              </tr>
            </thead>

            <tbody>
              {transactions.map(transaction => (
                <tr key={transaction.id}>
                  <td className="title">{transaction.title}</td>
                  <td className={transaction.type}>
                    {transaction.formattedValue}
                  </td>
                  <td>{transaction.category.title}</td>
                  <td>{transaction.formattedDate}</td>
                </tr>
              ))}
            </tbody>
          </table>
        </TableContainer>
      </Container>
    </>
  );
};

export default Dashboard;
